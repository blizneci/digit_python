# Напиши программу,котора считае входные данные и напечатает их,
#преобразовав в верхний регистр. Преобразовать строку в верхний регист  можжно
#с помощью метода .upper
#Sample input: какая-то строка
#Sample output: КАКАЯ-ТО СТРОКА

sample_string = input("Введите строку в нижнем регистре: ")
