print(f"Строка в верхнер регистре: {sample_string.upper()}")


#Упражнение 5.1
#Напиши программу,которая принимает на вход длину стороны квадрата (целое число) и 
#выводит на печать периметр этого квадрата.
#Для преобразования строки в число используй int()
#Sample Input: 100
#Sample Output: 400

square_side_size = int(input("Введите размер стороны квадрата: "))
print(f"Введенный размер стороны квадрата: {square_side_size}")
square_perimeter = square_side_size * 4
print(f"Периметр квадрата: {square_perimeter}")

